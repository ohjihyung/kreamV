package kr.co.kreamv.vo;

public class ConfirmVO {

	private String confirm_id    ;
	private String mem_id        ;
	private String detail_id     ;
	private String confirm_score ;
	private String confirm_state ;
	
	public String getConfirm_id() {
		return confirm_id;
	}
	public void setConfirm_id(String confirm_id) {
		this.confirm_id = confirm_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getDetail_id() {
		return detail_id;
	}
	public void setDetail_id(String detail_id) {
		this.detail_id = detail_id;
	}
	public String getConfirm_score() {
		return confirm_score;
	}
	public void setConfirm_score(String confirm_score) {
		this.confirm_score = confirm_score;
	}
	public String getConfirm_state() {
		return confirm_state;
	}
	public void setConfirm_state(String confirm_state) {
		this.confirm_state = confirm_state;
	}
	
	
	
	
	

}
