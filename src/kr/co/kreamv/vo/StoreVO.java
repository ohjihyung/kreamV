package kr.co.kreamv.vo;

public class StoreVO {
	private String store_id;
	private String confirm_id;
	private String store_date;
	private int store_state;
	private int bid_check;
	
	public String getStore_id() {
		return store_id;
	}
	public void setStore_id(String store_id) {
		this.store_id = store_id;
	}
	public String getConfirm_id() {
		return confirm_id;
	}
	public void setConfirm_id(String confirm_id) {
		this.confirm_id = confirm_id;
	}
	public String getStore_date() {
		return store_date;
	}
	public void setStore_date(String store_date) {
		this.store_date = store_date;
	}
	public int getStore_state() {
		return store_state;
	}
	public void setStore_state(int store_state) {
		this.store_state = store_state;
	}
	public int getBid_check() {
		return bid_check;
	}
	public void setBid_check(int bid_check) {
		this.bid_check = bid_check;
	}
	

	
	
}
