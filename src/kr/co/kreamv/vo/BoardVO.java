package kr.co.kreamv.vo;

public class BoardVO {
	private String board_num    ;
	private String mem_id       ;
	private String board_title  ;
	private String board_cont   ;
	private String board_nm     ;
	private String board_dnm    ;
	private String board_date;
	
	
	

	public String getBoard_date() {
		return board_date;
	}
	public void setBoard_date(String board_date) {
		this.board_date = board_date;
	}
	public String getBoard_num() {
		return board_num;
	}
	public void setBoard_num(String board_num) {
		this.board_num = board_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getBoard_title() {
		return board_title;
	}
	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}
	public String getBoard_cont() {
		return board_cont;
	}
	public void setBoard_cont(String board_cont) {
		this.board_cont = board_cont;
	}
	public String getBoard_nm() {
		return board_nm;
	}
	public void setBoard_nm(String board_nm) {
		this.board_nm = board_nm;
	}
	public String getBoard_dnm() {
		return board_dnm;
	}
	public void setBoard_dnm(String board_dnm) {
		this.board_dnm = board_dnm;
	}
	
	
}
