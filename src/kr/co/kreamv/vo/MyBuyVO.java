package kr.co.kreamv.vo;

public class MyBuyVO {
	private String prod_id;
	private String prod_knm;
	private String prod_photo1;
	private String detail_size;
	private int order_price;
	private String order_date;
	private String primary_key;
	

	public String getProd_id() {
		return prod_id;
	}
	public void setProd_id(String prod_id) {
		this.prod_id = prod_id;
	}
	public String getProd_knm() {
		return prod_knm;
	}
	public void setProd_knm(String prod_knm) {
		this.prod_knm = prod_knm;
	}
	public String getProd_photo1() {
		return prod_photo1;
	}
	public void setProd_photo1(String prod_photo1) {
		this.prod_photo1 = prod_photo1;
	}
	public String getDetail_size() {
		return detail_size;
	}
	public void setDetail_size(String detail_size) {
		this.detail_size = detail_size;
	}
	public int getOrder_price() {
		return order_price;
	}
	public void setOrder_price(int order_price) {
		this.order_price = order_price;
	}
	public String getOrder_date() {
		return order_date;
	}
	public void setOrder_date(String order_date) {
		this.order_date = order_date;
	}
	public String getPrimary_key() {
		return primary_key;
	}
	public void setPrimary_key(String primary_key) {
		this.primary_key = primary_key;
	}
	
}
