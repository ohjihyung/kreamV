package kr.co.kreamv.vo;

public class SellBidVO {
	private String sell_bid_id;
	private String confirm_id;
	private String detail_id;
	private String sell_bid_sdate;
	private String sell_bid_fdate;
	private int sell_bid_price;
	private int sell_bid_state;
	
	public String getSell_bid_id() {
		return sell_bid_id;
	}
	public void setSell_bid_id(String sell_bid_id) {
		this.sell_bid_id = sell_bid_id;
	}
	public String getConfirm_id() {
		return confirm_id;
	}
	public void setConfirm_id(String confirm_id) {
		this.confirm_id = confirm_id;
	}
	public String getDetail_id() {
		return detail_id;
	}
	public void setDetail_id(String detail_id) {
		this.detail_id = detail_id;
	}
	public String getSell_bid_sdate() {
		return sell_bid_sdate;
	}
	public void setSell_bid_sdate(String sell_bid_sdate) {
		this.sell_bid_sdate = sell_bid_sdate;
	}
	public String getSell_bid_fdate() {
		return sell_bid_fdate;
	}
	public void setSell_bid_fdate(String sell_bid_fdate) {
		this.sell_bid_fdate = sell_bid_fdate;
	}
	public int getSell_bid_price() {
		return sell_bid_price;
	}
	public void setSell_bid_price(int sell_bid_price) {
		this.sell_bid_price = sell_bid_price;
	}
	public int getSell_bid_state() {
		return sell_bid_state;
	}
	public void setSell_bid_state(int sell_bid_state) {
		this.sell_bid_state = sell_bid_state;
	}
}
